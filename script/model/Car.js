class Car {
    marca
    modelo
    cor
    chassi
    anoFabricacao
    anoModelo
    quilometragem
    motor
    valor
    cambio
    constructor(marca, modelo, cor, chassi, anoFabricacao, anoModelo, quilometragem, motor, valor, cambio) {
        this.marca = marca
        this.modelo = modelo
        this.cor = cor
        this.chassi = chassi
        this.anoFabricacao = anoFabricacao
        this.anoModelo = anoModelo
        this.quilometragem = quilometragem
        this.motor = motor
        this.valor = valor
        this.cambio = cambio
    }
}